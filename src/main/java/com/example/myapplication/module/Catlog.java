package com.example.myapplication.module;

public class Catlog {

    private int Design_no;
    private String Design_name;
    private String color;
    private String fabric_type;

    public int getDesign_no() {
        return Design_no;
    }

    public void setDesign_no(int design_no) {
        Design_no = design_no;
    }

    public String getDesign_name() {
        return Design_name;
    }

    public void setDesign_name(String design_name) {
        Design_name = design_name;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getFabric_type() {
        return fabric_type;
    }

    public void setFabric_type(String fabric_type) {
        this.fabric_type = fabric_type;
    }
}
