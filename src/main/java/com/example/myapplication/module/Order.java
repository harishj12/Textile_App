package com.example.myapplication.module;

public class Order {

    private int Design_no;
    private String Design_name;
    private String Color;
    private int No_of_pieces;

    public int getDesign_no() {
        return Design_no;
    }

    public void setDesign_no(int design_no) {
        Design_no = design_no;
    }

    public String getDesign_name() {
        return Design_name;
    }

    public void setDesign_name(String design_name) {
        Design_name = design_name;
    }

    public String getColor() {
        return Color;
    }

    public void setColor(String color) {
        Color = color;
    }

    public int getNo_of_pieces() {
        return No_of_pieces;
    }

    public void setNo_of_pieces(int no_of_pieces) {
        No_of_pieces = no_of_pieces;
    }


}
